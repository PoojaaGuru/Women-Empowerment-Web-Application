   <p style="color: #DAF7A6;">Women's health refers to the branch of medicine that focuses on the treatment and diagnosis of diseases and conditions that affect a woman’s 	physical and emotional well-being.<br>

   	Health is an important factor that contributes to human wellbeing and economic growth.<br>

	Currently, women in India has to face numerous health issues, which ultimately affect the aggregate economy’s output. Addressing the gender, class or ethnic disparities that exist in healthcare and improving the health outcomes can contribute to economic gain through the creation of quality human capital and increased levels of savings and investment.<br>

	Women in India face issues like malnutrition, lack of maternal health, diseases like AIDS , breast cancer, domestic violence and many more.<br></p>

	<h3>Malnutrition:</h3><br>

	<p style="color: #DAF7A6;">Nutrition plays a major role in and individual’s overall health, psychological and physical health status is often dramatically impacted by the presence of malnutrition.<br>
	India has one of the highest rates of malnourished women among developing countries. A 2012 study by Tarozzi have found the nutritional intake of early adolescents to be approximately equal. However, it is seen that the rate of malnutrition increases for women as they enter adulthood<br>

	Maternal malnutrition has been associated with an increased risk of maternal mortality and also child birth defects. Addressing the issues of malnutrition would have a beneficial outcomes for women and children.<br></p>

	<h3>Lack of maternal health:</h3><br>

<p style="color: #DAF7A6;">The lack of maternal health contributes to the economic disparities of mothers and their children.<br>

Poor maternal health not only affects a child’s health in adverse ways but also decreases a woman’s ability to participate in economic activities. Therefore, national health programs such as the National Rural Health Mission (NRHM) and the Family Welfare Program have been created to address the maternal health care needs of women across India. Although India has witnessed dramatic growth over the last two decades, maternal mortality still remains high as in comparison to many developing nations.<br>

India contributes to nearly 20 percent of all maternal deaths worldwide between 1992 and 2006. The primary reasons for the high levels of maternal mortality are directly related to disparities of economic conditions and cultural constraints limiting access to care.<br>

However, maternal mortality is not identical across all of India or even a particular state urban areas often have lower overall maternal mortality due to the availability of adequate medical resources. For those states where there is higher literacy and growth rates tend to have greater maternal health and also lower infant mortality.<br>

Suicide:<br><br>
Suicide is a major problem in India. The suicide rate in India is five times higher than that of the developed world. Furthermore, the rate of suicide has been found to be higher in women as compared to men in India.<br></p>

<h3>The most common reasons for women's suicide is directly related to:</h3><br>
<p style="color: #DAF7A6;">
  1.Depression<br>2.Anxiety<br>3.Gender discrimination<br>4.Domestic violence<br>
</p>

<p style="color: #DAF7A6;">
The suicide rate is particularly high among female sex workers in India, who face numerous forms of discrimination for their gender and line of work.<br></p>

<h3>Domestic Violence:</h3><br>

<p style="color: #DAF7A6;">Domestic violence is a major issues in India. Domestic violence is defined as acts of physical, psychological, and sexual violence against women is found across the world and is currently viewed as a hidden epidemic by the World Health Organization.<br><br>

As per reports of India National Family Health Survey III (2005-2006), 31 percent of all women reported having been the victims of physical violence in the last 12 months. However, the actual number of victims may be much higher. The study found that the poorest women faired worst among middle and high-income women.<br></p>

<h3>Medical test for women in 20's & 30's:</h3><br>
  
	<p style="color: #DAF7A6;">Weighing weight: One should weigh regularly, as too much of it may puts you at high risk for developing a number of diseases later in life.<br>

	Blood Pressure. It's simple, it's cheap and it's quick.<br>

	Cholesterol Profile. One should also have a cholesterol test. Everyone age 20 and over should know their cholesterol numbers, and get them checked at least once every five years.<br>

    For Women Only: Breast Exam, Pelvic Exam and Pap. A clinical breast exam and 10 minutes of mild discomfort from the pelvic exam pays big dividends in protecting from cancer and diseases that can cause infertility. If you've had abnormal Paps in the past, be sure to get a new Pap smear as often as your doctor recommends. For those with no history of an abnormal Pap, a Pap smear can be done every three years, rather than annually.<br>

    Protecting Your Eyes. You may not have considered this, but at some point before you're 40, visit an eye care provider for an exam.<br>

    Checking Your Immunizations. Update any immunizations that you might need.<br>

